
public class loopDemo {

	public static void main(String[] args) {
		//For Döngüleri
		
		
		for(int i=1;i<=10;i++)
		{
			System.out.println(i);
		}
		System.out.println("For Döngüsü Bitti \n");
		
		//While Döngüleri
		
		int i=2;
		while(i<10) {
			System.out.println(i);
			i+=2;
		}
		System.out.println("While Döngüsü Bitti \n");
		
		// DO-WHİLE Döngüleri 
		//Do While her zaman bir kere çalışır 
		int j=1;
		do {
				System.out.println(j);
				j+=2;
			}while(j<10);
		System.out.println("DO-WHİLE Döngüsü Bitti \n");
		

	}

}
